import { getMacroEventsForDate, MACRO_META } from './macroCalendar'
import { getSqDates, getSqMarkersForDate, SQ_META } from './sqCalendar'

function nextBusinessDays(from: Date, count: number): Date[] {
  const days: Date[] = []
  const d = new Date(from)
  d.setHours(0, 0, 0, 0)
  while (days.length < count) {
    d.setDate(d.getDate() + 1)
    const dow = d.getDay()
    if (dow !== 0 && dow !== 6) days.push(new Date(d))
  }
  return days
}

export function buildUpcomingEventsText(businessDays = 5): string {
  const today = new Date()
  today.setHours(0, 0, 0, 0)
  const DAYS_JA = ['日', '月', '火', '水', '木', '金', '土']
  const sqDates = [
    ...getSqDates(today.getFullYear()),
    ...getSqDates(today.getFullYear() + 1),
  ]
  const lines: string[] = []
  for (const d of nextBusinessDays(today, businessDays)) {
    const events: string[] = []
    for (const e of getMacroEventsForDate(d, { us: true, jp: true })) {
      events.push(MACRO_META[e.type].label)
    }
    for (const sq of getSqMarkersForDate(d, sqDates)) {
      events.push(SQ_META[sq].label)
    }
    if (events.length > 0) {
      lines.push(`${d.toISOString().slice(0, 10)}（${DAYS_JA[d.getDay()]}）: ${events.join('、')}`)
    }
  }
  return lines.length > 0 ? lines.join('\n') : 'イベントなし'
}

